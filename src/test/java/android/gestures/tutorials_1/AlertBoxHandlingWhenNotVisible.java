public class AlertBoxHandlingWhenNotVisible {

	public static void main(String[] args) {
		
		/* 
		 * Handling Alert Box when they are not visible 
		 * Some of the alert box are sometime visible and sometime not visible. In that case we will
		 * write a try block where we will set the switch to alert code and in the catch block we write 
		 * the statements when alert box will not be visible.
		 * So, when alert box is visible it will execute the try block code and when not visible it will go to
		 * catch block and handle the exception and we can print alert box is not visible;
		 * 
		 * */
		
		try
		{
			// code for handling the alert box;
		}
		catch(Exception e)
		{
			//code for handling the exception;
		}

	}

}
